package Lab_2;

public class UseDoWhile {
    public static void main(String[] args){
        int choice = 5;
        do{
            System.out.println("----Welcome to the world of Bharat Karle----");
            System.out.println("Are you happy");
            System.out.println("Enjoy the life!");
            choice++;
        }
        while(choice<5);
    }

}
